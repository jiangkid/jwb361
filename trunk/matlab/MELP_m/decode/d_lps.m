function [f,state_syn]=d_lps(lpc_coeff,ae,T,state_syn)

% Синтезирующий LPC фильтр
% ВХОДНЫЕ ПАРАМЕТРЫ:
%	lpc_coeff - интерполированные коэффициенты ЛП
%	ae        - сигнал с выхода блока адаптивной спектральной коррекции
%	T         - значение задержки ОТ
%	state_syn - начальное состояние фильтра синтеза
% ВЫХОДНЫЕ ПАРАМЕТРЫ: 
%	f         - синтезированный РС
%	state_syn - конечное состояние фильтра синтеза

[f,state_syn]=filter(1,[1,lpc_coeff],ae,state_syn); 
