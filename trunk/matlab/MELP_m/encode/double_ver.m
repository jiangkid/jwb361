function cor_p=double_ver(sig_in,pp,cor_p)
% Удаление низкого ОТ  
% ВХОДНЫЕ ПЕРЕМЕННЫЕ: 
%   sig_in - входной сигнал
%   pp     - значение ОТ
%   cor_p  - корреляция 
% ВЫХОДНАЯ ПЕРЕМЕННАЯ:  
% cor_p    - выходная корреляция 

[np,ncor_p]=fpr(sig_in,round(2*pp)); % вычисление корреляции для 
                                     % удвоенного значения ОТ 
if ncor_p<cor_p    % Если рассчитанное значение корреляции меньше входной,
    cor_p=ncor_p;  % то оно присваивается выходной
end